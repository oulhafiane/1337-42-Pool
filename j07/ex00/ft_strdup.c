#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0' || dest[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*p;
	int		size;

	size = ft_strlen(src);
	p = (char*)malloc(sizeof(char) * size + 100);
	if (p == 0)
		return (p);
	ft_strcpy(p, src);
	return (p);
}
